package com.atovio.junit.service;

/**
 * 数学业务类
 *
 * @version 1.0
 * @Organization AtoVio
 * @Author Winter Yuan
 */
public class MathService {

    /**
     * 求和
     *
     * @param a
     * @param b
     * @return
     */
    public int sum(int a, int b) {
        return a + b;
    }

    /**
     * 求差
     *
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b) {
        return a - b;
    }
}
